package co.yedam.inheritance;

// 부모 클래스

public class Student {
	private String name;
	private int studNo;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStudNo() {
		return studNo;
	}

	public void setStudNo(int studNo) {
		this.studNo = studNo;
	}

}
